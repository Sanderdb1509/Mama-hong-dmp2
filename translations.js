// Language state
let currentLanguage = 'en';

// Initialize language from localStorage or default to English
function initLanguage() {
    const saved = localStorage.getItem('mamaHongLang');
    if (saved && translations[saved]) {
        currentLanguage = saved;
    }
    applyTranslations();
    updateActiveFlag();
}

// Set specific language
function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('mamaHongLang', currentLanguage);
        applyTranslations();
        updateActiveFlag();
    }
}

// Update active flag styling
function updateActiveFlag() {
    const flagButtons = document.querySelectorAll('.flag-button');
    flagButtons.forEach(btn => {
        const lang = btn.getAttribute('data-lang');
        if (lang === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Apply translations to all elements with data-translate attribute
function applyTranslations() {
    const t = translations[currentLanguage];
    
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.innerHTML = t[key];
            }
        }
    });
    
    // Update html lang attribute
    document.documentElement.lang = currentLanguage === 'nl' ? 'nl' : currentLanguage === 'vi' ? 'vi' : 'en';
}

// Setup event listeners when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    initLanguage();
    
    // Add click handlers to all flag buttons
    const flagButtons = document.querySelectorAll('.flag-button');
    flagButtons.forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            if (lang) {
                setLanguage(lang);
            }
        });
    });
});
